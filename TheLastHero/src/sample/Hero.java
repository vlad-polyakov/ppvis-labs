package sample;

public class Hero {
    private int heroCash;
    private int round;
    private int roundCash;
    public int getHeroCash() {
        return heroCash;
    }

    public int getRoundCash() {
        return roundCash;
    }

    public void setRoundCash(int roundCash) {
        this.roundCash = roundCash;
    }

    public void setHeroCash(int heroCash) {
        this.heroCash = heroCash;
    }

    public int getRound() {
        return round;
    }

    public void setRound(int round) {
        this.round = round;
    }
}
